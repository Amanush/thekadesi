package dolly;

public class swap {
	public static void main(String[]args)
	{
		int a,b;
		a=1;
		b=2;
		System.out.println(" Before Swapping[exchange]" );
		System.out.println( "a="+a+"b="+b);
		a=a+b;
		b=a-b;
		a=a-b;
		System.out.println("After Swapping[exchange]");
		System.out.println(" a="+a+"b="+b);
		
	}
	

}
